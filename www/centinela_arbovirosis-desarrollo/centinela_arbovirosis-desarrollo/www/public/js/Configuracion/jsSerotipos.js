const listarSerotipos = (datas) => {
	return new Promise((resolve, reject) => {

		fetch('Serotipos/listarSerotipos', {
				method: 'POST',
				body: datas
			})
			.then(respuesta => respuesta.json())
			.then(datos => {
				resolve(datos);
			})
			.catch(msg => {
				reject(msg)
			})
	})
}


